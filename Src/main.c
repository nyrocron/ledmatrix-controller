void udelay(uint32_t delay)
{
  uint32_t ticks_start = __HAL_TIM_GET_COUNTER(&htim2);
  uint32_t ticks_delay = delay * ticks_per_us;
  while (__HAL_TIM_GET_COUNTER(&htim2) - ticks_start < ticks_delay) ;
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  if (htim == &htim3) {
    HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_11);  // For debugging

    matrix_update(&mat1);
  }

  __HAL_TIM_CLEAR_IT(htim, TIM_IT_UPDATE);
}

void HAL_SPI_RxCpltCallback(SPI_HandleTypeDef *hspi)
{
  switch (state) {
  case STATE_RX:
    SPI1_RxAddr(hspi);
    break;
  case STATE_SETTEXT_WAITLEN:
    // length received, start text transfer
    state = STATE_SETTEXT_XFTEXT;
    break;
  case STATE_SETTEXT_WAITTEXT:
    // text transfer complete, start new rx
    state = STATE_NONE;
    break;
  }
}

void SPI1_RxAddr(SPI_HandleTypeDef *hspi)
{
  switch (addr_buf) {
   case ADDR_SETTEXT:
     // address was for SETTEXT, start length transfer
     state = STATE_SETTEXT_XFLEN;
     break;
   default:
     // unknown address, start new rx
     state = STATE_NONE;
     break;
   }
}

